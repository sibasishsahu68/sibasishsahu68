list = [10, 20, 30]
print("List elements:")
for item in list:
    print(item)

tuple = ("Apple", "Banana", "Watermelon")
print("\nTuple elements:")
for item in tuple:
    print(item)

dict = {"Name": "Dev", "Age": 19, "State": "Odisha"}
print("\nDictionary elements are:")
for key in dict:
    print(key, ":", dict[key])

set = (1, 2, 3, 4)
print("\nSet elements:")
for item in set:
    print(item)